// 1. กำหนดตัวแปร DOM Elements
const drawButton = document.getElementById('draw-oracle-btn');
const resetButton = document.getElementById('reset-button');
const resultSection = document.getElementById('result-section');
const chiChiJar = document.getElementById('chi-chi-stick-img');
const oracleAnimationDiv = document.querySelector('.oracle-animation');

// 2. ฟังก์ชันหลักในการสุ่มเซียมซีและแสดงผล
function drawOracle() {
    // ป้องกันการสุ่มซ้ำ
    drawButton.disabled = true; // ปิดการทำงานของปุ่ม (แม้จะซ่อนอยู่)
    chiChiJar.style.pointerEvents = 'none'; // ปิดการคลิกซ้ำที่รูปภาพ

    // 1. เพิ่ม Animation เขย่า
    chiChiJar.classList.add('shaking');
    
    // หน่วงเวลาเพื่อให้ Animation แสดงผล (ประมาณ 1.5 วินาที)
    setTimeout(() => {
        // 2. สุ่มเลข
        const totalOracles = chiChiOracles.length;
        const randomIndex = Math.floor(Math.random() * totalOracles);
        const resultOracle = chiChiOracles[randomIndex];

        // 3. หยุด Animation และซ่อนส่วนสุ่ม
        chiChiJar.classList.remove('shaking');
        oracleAnimationDiv.style.display = 'none'; // ซ่อนกระบอกเซียมซี
        
        // 4. แสดงผลลัพธ์
        displayResult(resultOracle);

    }, 1500); // 1.5 วินาที สำหรับเขย่า
}

// 3. ฟังก์ชันแสดงผลคำทำนาย
function displayResult(oracle) {
    document.getElementById('oracle-number').textContent = oracle.number;
    document.getElementById('oracle-title').textContent = oracle.title;
    document.getElementById('oracle-summary').textContent = oracle.summary;
    document.getElementById('oracle-full-text').textContent = oracle.oracle_text;

    // แสดงส่วนผลลัพธ์
    resultSection.classList.remove('hidden');
    
    // เลื่อนหน้าจอไปที่ผลลัพธ์
    resultSection.scrollIntoView({ behavior: 'smooth' });
}

// 4. ฟังก์ชันรีเซ็ตเพื่อสุ่มใหม่
function resetOracle() {
    // ซ่อนส่วนผลลัพธ์
    resultSection.classList.add('hidden');
    
    // แสดงส่วนสุ่ม (กระบอกเซียมซี) กลับมา
    oracleAnimationDiv.style.display = 'flex'; 
    drawButton.disabled = false;
    chiChiJar.style.pointerEvents = 'auto'; // เปิดการคลิกที่รูปภาพ
    
    // เลื่อนกลับไปด้านบน
    window.scrollTo({ top: 0, behavior: 'smooth' });
}

// 5. การผูก Event Listeners (รูปภาพเป็นตัวหลักในการสั่งงาน)
chiChiJar.addEventListener('click', drawOracle);
resetButton.addEventListener('click', resetOracle);